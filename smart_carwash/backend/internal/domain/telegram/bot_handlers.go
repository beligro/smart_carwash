// HandleCallbackQuery –æ–±—Ä–∞–±–∞—Ç—ã–≤–∞–µ—Ç –Ω–∞–∂–∞—Ç–∏—è –Ω–∞ inline –∫–Ω–æ–ø–∫–∏
func (h *BotHandler) HandleCallbackQuery(update tgbotapi.Update) {
	callback := update.CallbackQuery
	if callback == nil {
		return
	}

	log.Printf("–ü–æ–ª—É—á–µ–Ω callback: %s", callback.Data)

	switch callback.Data {
	case "open_mini_app", "open_mini_app_success", "open_mini_app_fail":
		h.openMiniApp(callback)
	default:
		// –û—Ç–≤–µ—á–∞–µ–º –Ω–∞ –Ω–µ–∏–∑–≤–µ—Å—Ç–Ω—ã–π callback
		h.bot.Request(tgbotapi.NewCallback(callback.ID, "–ù–µ–∏–∑–≤–µ—Å—Ç–Ω–∞—è –∫–æ–º–∞–Ω–¥–∞"))
	}
}

// openMiniApp –æ—Ç–∫—Ä—ã–≤–∞–µ—Ç Mini App
func (h *BotHandler) openMiniApp(callback *tgbotapi.CallbackQuery) {
	// –°–æ–∑–¥–∞–µ–º –æ–±—ã—á–Ω—É—é –∫–Ω–æ–ø–∫—É –¥–ª—è –æ—Ç–∫—Ä—ã—Ç–∏—è Mini App
	keyboard := tgbotapi.NewInlineKeyboardMarkup(
		tgbotapi.NewInlineKeyboardRow(
			tgbotapi.NewInlineKeyboardButtonData(
				"üöó –û—Ç–∫—Ä—ã—Ç—å –ø—Ä–∏–ª–æ–∂–µ–Ω–∏–µ",
				"open_mini_app",
			),
		),
	)

	// –û—Ç–ø—Ä–∞–≤–ª—è–µ–º –Ω–æ–≤–æ–µ —Å–æ–æ–±—â–µ–Ω–∏–µ —Å –∫–Ω–æ–ø–∫–æ–π
	msg := tgbotapi.NewMessage(callback.Message.Chat.ID, "–ù–∞–∂–º–∏—Ç–µ –∫–Ω–æ–ø–∫—É –Ω–∏–∂–µ, —á—Ç–æ–±—ã –æ—Ç–∫—Ä—ã—Ç—å –ø—Ä–∏–ª–æ–∂–µ–Ω–∏–µ:")
	msg.ReplyMarkup = keyboard

	if _, err := h.bot.Send(msg); err != nil {
		log.Printf("–û—à–∏–±–∫–∞ –æ—Ç–ø—Ä–∞–≤–∫–∏ –∫–Ω–æ–ø–∫–∏: %v", err)
		h.bot.Request(tgbotapi.NewCallback(callback.ID, "–û—à–∏–±–∫–∞ –æ—Ç–∫—Ä—ã—Ç–∏—è –ø—Ä–∏–ª–æ–∂–µ–Ω–∏—è"))
		return
	}

	// –û—Ç–≤–µ—á–∞–µ–º –Ω–∞ callback
	h.bot.Request(tgbotapi.NewCallback(callback.ID, "–û—Ç–∫—Ä—ã–≤–∞—é –ø—Ä–∏–ª–æ–∂–µ–Ω–∏–µ..."))
} 